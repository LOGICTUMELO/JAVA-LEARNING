public class IfElseIf {
    public static void main(String[] args) {
        int EatingTime=19;

        if(EatingTime<10){
           System.out.println("Breakfast time");
        }else if(EatingTime<18){
           System.out.println("Lunch time");
        }else{
            System.out.println("Supper time");
        }
    }
}
