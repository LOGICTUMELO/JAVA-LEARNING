public class Switcher {
    public static void main(String[] args) {
        int Day=5;
        switch(Day){
            case 1:
            System.out.println("It is monday and it is boring");
            break;

            case 3:
            System.out.println("It is wednesday...you wanna go out skating?");
            break;

            case 5:
            System.out.println("It is friday...you should go to the freshers party");
            break;

            case 7:
            System.out.println("It is sunday dont you wanna go to church?");
            break;

            default:
            System.out.println("I do not know what today is man!!");
        }
    }
}
